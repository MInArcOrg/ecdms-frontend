import { AxiosResponse } from 'axios';
import { DesignTrafficFlow } from 'src/types/general/general-master';
import { GetRequestParam, IApiPayload, IApiResponse } from 'src/types/requests';
import axiosServices from 'src/utils/axios';
import { buildGetRequest } from 'src/utils/requests/get-request';
import { buildPostRequest } from 'src/utils/requests/post-request';
import { buildPutRequest } from 'src/utils/requests/put-request';

const designTrafficFlowMasterService = {
  getAll: (params: GetRequestParam): Promise<IApiResponse<DesignTrafficFlow[]>> =>
    buildGetRequest(`/masterdata/design-traffic-flows`, params)
      .then((response: AxiosResponse<IApiResponse>) => response.data)
      .catch((error: any) => {
        throw error;
      }),

  getOne: (idx: string, params: GetRequestParam): Promise<IApiResponse> =>
    buildGetRequest(`/masterdata/design-traffic-flows/${idx}`, params)
      .then((response: AxiosResponse<IApiResponse>) => response.data)
      .catch((error: any) => {
        throw error;
      }),
  searchResource: (params: GetRequestParam) =>
    buildGetRequest(`/masterdata/design-traffic-flows-search`, params)
      .then((response: AxiosResponse<IApiResponse>) => response.data.payload as unknown as DesignTrafficFlow[])
      .catch((error: any) => {
        throw error;
      }),
  delete: (idx: string): Promise<IApiResponse> =>
    axiosServices
      .delete(`/masterdata/design-traffic-flows/${idx}`)
      .then((response: AxiosResponse<IApiResponse>) => response.data)
      .catch((error: any) => {
        throw error;
      }),

  create: (body: IApiPayload<DesignTrafficFlow>): Promise<IApiResponse> =>
    buildPostRequest(`/masterdata/design-traffic-flows`, body, false)
      .then((response: AxiosResponse<IApiResponse>) => response.data)
      .catch((error: any) => {
        throw error;
      }),
  update: (id: string, body: IApiPayload<DesignTrafficFlow>): Promise<IApiResponse> =>
    buildPutRequest(`/masterdata/design-traffic-flows/${id}`, body)
      .then((response: AxiosResponse<IApiResponse>) => response.data)
      .catch((error: any) => {
        throw error;
      })
};

export default designTrafficFlowMasterService;
