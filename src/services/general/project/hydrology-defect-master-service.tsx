import { AxiosResponse } from 'axios';
import { HydrologyDefect } from 'src/types/general/general-master';
import { GetRequestParam, IApiPayload, IApiResponse } from 'src/types/requests';
import axiosServices from 'src/utils/axios';
import { buildGetRequest } from 'src/utils/requests/get-request';
import { buildPostRequest } from 'src/utils/requests/post-request';
import { buildPutRequest } from 'src/utils/requests/put-request';

const hydrologyDefectMasterService = {
  getAll: (params: GetRequestParam): Promise<IApiResponse<HydrologyDefect[]>> =>
    buildGetRequest(`/masterdata/hydrology-defects`, params)
      .then((response: AxiosResponse<IApiResponse>) => response.data)
      .catch((error: any) => {
        throw error;
      }),

  getOne: (idx: string, params: GetRequestParam): Promise<IApiResponse> =>
    buildGetRequest(`/masterdata/hydrology-defects/${idx}`, params)
      .then((response: AxiosResponse<IApiResponse>) => response.data)
      .catch((error: any) => {
        throw error;
      }),
  searchResource: (params: GetRequestParam) =>
    buildGetRequest(`/masterdata/hydrology-defects-search`, params)
      .then((response: AxiosResponse<IApiResponse>) => response.data.payload as unknown as HydrologyDefect[])
      .catch((error: any) => {
        throw error;
      }),
  delete: (idx: string): Promise<IApiResponse> =>
    axiosServices
      .delete(`/masterdata/hydrology-defects/${idx}`)
      .then((response: AxiosResponse<IApiResponse>) => response.data)
      .catch((error: any) => {
        throw error;
      }),

  create: (body: IApiPayload<HydrologyDefect>): Promise<IApiResponse> =>
    buildPostRequest(`/masterdata/hydrology-defects`, body, false)
      .then((response: AxiosResponse<IApiResponse>) => response.data)
      .catch((error: any) => {
        throw error;
      }),
  update: (id: string, body: IApiPayload<HydrologyDefect>): Promise<IApiResponse> =>
    buildPutRequest(`/masterdata/hydrology-defects/${id}`, body)
      .then((response: AxiosResponse<IApiResponse>) => response.data)
      .catch((error: any) => {
        throw error;
      })
};

export default hydrologyDefectMasterService;
